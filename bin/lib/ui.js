// в”Җв”Җ Table в”Җв”Җв”Җв”Җв”Җв”Җв”Җв”Җв”Җв”Җв”Җв”Җв”Җв”Җв”Җв”Җв”Җв”Җв”Җв”Җв”Җв”Җв”Җв”Җв”Җв”Җв”Җв”Җв”Җв”Җв”Җв”Җв”Җв”Җв”Җв”Җв”Җв”Җв”Җв”Җв”Җв”Җв”Җв”Җв”Җв”Җв”Җв”Җв”Җв”Җв”Җв”Җв”Җв”Җв”Җв”Җв”Җв”Җв”Җв”Җв”Җв”Җв”Җв”Җв”Җв”Җв”Җ
// Renders aligned columns with header separators.
export function table(headers, rows, { indent = 2 } = {}) {
  // Calculate column widths from headers + data
  const colWidths = headers.map((h, i) => {
    const dataMax = rows.reduce((max, row) => {
      const cell = String(row[i] ?? '');
      return Math.max(max, stripAnsi(cell).length);
    }, 0);
    return Math.max(stripAnsi(h).length, dataMax) + 2;
  });

  const pad = ' '.repeat(indent);

  // Header
  const headerLine = headers.map((h, i) => C.gold(h.padEnd(colWidths[i]))).join('');
  console.log(pad + headerLine);

  // Separator
  const sep = colWidths.map(w => 'в”Җ'.repeat(w)).join('');
  console.log(pad + C.border(sep));

  // Rows
  for (const row of rows) {
    const line = row.map((cell, i) => {
      const str = String(cell ?? '');
      const visible = stripAnsi(str);
      const fill = Math.max(0, colWidths[i] - visible.length);
      return str + ' '.repeat(fill);
    }).join('');
    console.log(pad + line);
  }
}

// в”Җв”Җ Progress bar в”Җв”Җв”Җв”Җв”Җв”Җв”Җв”Җв”Җв”Җв”Җв”Җв”Җв”Җв”Җв”Җв”Җв”Җв”Җв”Җв”Җв”Җв”Җв”Җв”Җв”Җв”Җв”Җв”Җв”Җв”Җв”Җв”Җв”Җв”Җв”Җв”Җв”Җв”Җв”Җв”Җв”Җв”Җв”Җв”Җв”Җв”Җв”Җв”Җв”Җв”Җв”Җв”Җв”Җв”Җв”Җв”Җв”Җв”Җв”Җ
// [в–Ҳв–Ҳв–Ҳв–Ҳв–Ҳв–Ҳв–Ҳв–Ҳв–‘в–‘в–‘в–‘в–‘в–‘в–‘в–‘в–‘в–‘в–‘в–‘] 40%
export function progressBar(current, total, { width = 30, label = '' } = {}) {
  const pct = Math.round((current / total) * 100);
  const filled = Math.round((current / total) * width);
  const empty = width - filled;
  const bar = C.gold('в–Ҳ'.repeat(filled)) + C.dim('в–‘'.repeat(empty));
  const pctStr = C.white(`${pct}%`.padStart(4));
  const labelStr = label ? C.dim(` ${label}`) : '';
  process.stdout.write(`\r  [${bar}] ${pctStr}${labelStr}`);
  if (current >= total) process.stdout.write('\n');
}

// в”Җв”Җ Spinner (simple, single-frame replacement) в”Җв”Җв”Җв”Җв”Җв”Җв”Җв”Җв”Җв”Җв”Җв”Җв”Җв”Җв”Җв”Җв”Җв”Җв”Җв”Җв”Җв”Җв”Җв”Җв”Җв”Җв”Җв”Җв”Җв”Җ
const SPIN_FRAMES = ['в Ӣ', 'в ҷ', 'в №', 'в ё', 'в ј', 'в ҙ', 'в Ұ', 'в §', 'в Ү', 'в Ҹ'];

export function spinner(message) {
  let i = 0;
  let active = true;
  const interval = setInterval(() => {
    if (!active) return;
    process.stdout.write(`\r  ${C.gold(SPIN_FRAMES[i % SPIN_FRAMES.length])} ${message}`);
    i++;
  }, 80);

  return {
    stop(finalMessage) {
      active = false;
      clearInterval(interval);
      process.stdout.write(`\r  ${ICON.ok} ${finalMessage || message}\n`);
    },
    fail(finalMessage) {
      active = false;
      clearInterval(interval);
      process.stdout.write(`\r  ${ICON.fail} ${finalMessage || message}\n`);
    },
  };
}

