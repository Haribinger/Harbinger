// в”Җв”Җ Box drawing в”Җв”Җв”Җв”Җв”Җв”Җв”Җв”Җв”Җв”Җв”Җв”Җв”Җв”Җв”Җв”Җв”Җв”Җв”Җв”Җв”Җв”Җв”Җв”Җв”Җв”Җв”Җв”Җв”Җв”Җв”Җв”Җв”Җв”Җв”Җв”Җв”Җв”Җв”Җв”Җв”Җв”Җв”Җв”Җв”Җв”Җв”Җв”Җв”Җв”Җв”Җв”Җв”Җв”Җв”Җв”Җв”Җв”Җв”Җв”Җв”Җ
// Draws a Unicode box around content lines.
//   в”Ңв”Җв”Җ TITLE в”Җв”Җв”Җв”Җв”Җв”Җв”Җв”Җв”Җв”Җв”Җв”Җв”Җв”Җв”Җв”Җв”Җв”Җв”Җв”Җв”Җв”Җв”Җв”Җв”җ
//   в”Ӯ  line 1                         в”Ӯ
//   в”Ӯ  line 2                         в”Ӯ
//   в””в”Җв”Җв”Җв”Җв”Җв”Җв”Җв”Җв”Җв”Җв”Җв”Җв”Җв”Җв”Җв”Җв”Җв”Җв”Җв”Җв”Җв”Җв”Җв”Җв”Җв”Җв”Җв”Җв”Җв”Җв”Җв”Җв”Җв”ҳ

function stripAnsi(str) {
  // eslint-disable-next-line no-control-regex
  return str.replace(/\x1b\[[0-9;]*m/g, '');
}

export function box(title, lines, { width = 56, padding = 2 } = {}) {
  const pad = ' '.repeat(padding);
  const innerW = width - 2; // minus left/right border chars

  // Top border
  const titleStr = title ? ` ${title} ` : '';
  const topFill = 'в”Җ'.repeat(Math.max(0, innerW - titleStr.length));
  console.log(C.border('в”Ңв”Җв”Җ') + C.gold(titleStr) + C.border(topFill + 'в”җ'));

  // Content lines
  for (const line of lines) {
    const visible = stripAnsi(line);
    const fillLen = Math.max(0, innerW - padding * 2 - visible.length);
    console.log(C.border('в”Ӯ') + pad + line + ' '.repeat(fillLen) + pad + C.border('в”Ӯ'));
  }

  // Bottom border
  console.log(C.border('в””' + 'в”Җ'.repeat(innerW) + 'в”ҳ'));
}

