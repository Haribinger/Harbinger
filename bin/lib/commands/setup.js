/**
 * harbinger setup / setup-platform / setup-telegram / setup-discord / reset-auth
 *
 * Tries the dedicated setup scripts first, falls back to inline implementations
 * so the CLI always works even without the setup/ directory.
 */

import { execSync } from 'child_process';
import fs from 'fs';
import path from 'path';
import { fileURLToPath } from 'url';
import { C, ICON, box, nl, section, kv } from '../ui.js';

const __filename = fileURLToPath(import.meta.url);
const __dirname = path.dirname(__filename);
const packageDir = path.join(__dirname, '..', '..', '..');

function trySetupScript(scriptName) {
  const scriptPath = path.join(packageDir, 'setup', scriptName);
  if (!fs.existsSync(scriptPath)) return false;
  try {
    execSync(`node ${scriptPath}`, { stdio: 'inherit', cwd: process.cwd() });
    return true;
  } catch {
    return false;
  }
}

function updateEnv(key, val) {
  const envPath = path.join(process.cwd(), '.env');
  let content = fs.existsSync(envPath) ? fs.readFileSync(envPath, 'utf-8') : '';
  const re = new RegExp(`^${key}=.*$`, 'm');
  if (re.test(content)) {
    content = content.replace(re, `${key}=${val}`);
  } else {
    content = content.trimEnd() + `\n${key}=${val}\n`;
  }
  fs.writeFileSync(envPath, content);
}

function readEnv(key) {
  const envPath = path.join(process.cwd(), '.env');
  if (!fs.existsSync(envPath)) return '';
  const content = fs.readFileSync(envPath, 'utf-8');
  const match = content.match(new RegExp(`^${key}=(.+)$`, 'm'));
  return match ? match[1].trim() : '';
}

// ── harbinger setup ─────────────────────────────────────────────────────────
export async function setup() {
  if (trySetupScript('setup-hub.mjs')) return;

  // Inline fallback: interactive setup wizard
  const { text, select, password, isCancel } = await import('@clack/prompts');
  const { randomBytes } = await import('crypto');

  box('HARBINGER SETUP', [
    C.dim('Interactive configuration wizard'),
    C.dim('All values saved to .env — no manual editing needed'),
  ]);
  nl();

  // Ensure .env exists
  const envPath = path.join(process.cwd(), '.env');
  if (!fs.existsSync(envPath)) {
    fs.writeFileSync(envPath, '# Harbinger — generated by harbinger setup\n');
    console.log(`  ${ICON.ok} Created .env`);
  }

  // Generate secrets if missing
  if (!readEnv('JWT_SECRET')) {
    updateEnv('JWT_SECRET', randomBytes(32).toString('hex'));
    console.log(`  ${ICON.ok} Generated JWT_SECRET`);
  }
  if (!readEnv('DB_PASSWORD')) {
    updateEnv('DB_PASSWORD', randomBytes(16).toString('hex'));
    console.log(`  ${ICON.ok} Generated DB_PASSWORD`);
  }
  if (!readEnv('REDIS_PASSWORD')) {
    updateEnv('REDIS_PASSWORD', randomBytes(16).toString('hex'));
    console.log(`  ${ICON.ok} Generated REDIS_PASSWORD`);
  }
  if (!readEnv('NEO4J_PASSWORD')) {
    updateEnv('NEO4J_PASSWORD', randomBytes(16).toString('hex'));
    console.log(`  ${ICON.ok} Generated NEO4J_PASSWORD`);
  }
  if (!readEnv('AUTH_SECRET')) {
    updateEnv('AUTH_SECRET', randomBytes(32).toString('base64'));
    console.log(`  ${ICON.ok} Generated AUTH_SECRET`);
  }

  nl();

  // AI Provider
  section('AI PROVIDER');
  const provider = await select({
    message: 'Which AI provider?',
    options: [
      { label: 'Ollama (Local — recommended)', value: 'ollama' },
      { label: 'Anthropic (Claude)', value: 'anthropic' },
      { label: 'OpenAI (GPT-4)', value: 'openai' },
      { label: 'Groq (Fast inference)', value: 'groq' },
      { label: 'Google Gemini', value: 'gemini' },
      { label: 'Mistral AI', value: 'mistral' },
      { label: 'LM Studio (Local)', value: 'lmstudio' },
      { label: 'GPT4All (Offline)', value: 'gpt4all' },
      { label: 'Custom endpoint', value: 'custom' },
    ],
  });
  if (isCancel(provider)) { console.log('\nCancelled.\n'); process.exit(0); }

  updateEnv('LLM_PROVIDER', provider);

  if (provider === 'ollama') {
    const url = await text({ message: 'Ollama URL:', defaultValue: 'http://localhost:11434' });
    if (isCancel(url)) { console.log('\nCancelled.\n'); process.exit(0); }
    updateEnv('OLLAMA_URL', url);
    console.log(`  ${ICON.ok} Ollama configured at ${url}`);
  } else if (provider === 'lmstudio') {
    const url = await text({ message: 'LM Studio URL:', defaultValue: 'http://localhost:1234/v1' });
    if (isCancel(url)) { console.log('\nCancelled.\n'); process.exit(0); }
    updateEnv('OLLAMA_URL', url);
  } else if (provider === 'gpt4all') {
    const url = await text({ message: 'GPT4All URL:', defaultValue: 'http://localhost:4891/v1' });
    if (isCancel(url)) { console.log('\nCancelled.\n'); process.exit(0); }
    updateEnv('OLLAMA_URL', url);
  } else if (provider !== 'custom') {
    const key = await password({ message: `${provider} API key:` });
    if (isCancel(key)) { console.log('\nCancelled.\n'); process.exit(0); }
    if (key) updateEnv('LLM_API_KEY', key);
  } else {
    const url = await text({ message: 'Custom API base URL:' });
    if (isCancel(url)) { console.log('\nCancelled.\n'); process.exit(0); }
    updateEnv('OLLAMA_URL', url);
    const key = await password({ message: 'API key (optional):' });
    if (!isCancel(key) && key) updateEnv('LLM_API_KEY', key);
  }

  // GitHub
  section('GITHUB AUTH');
  console.log(`  ${C.dim('Optional — you can skip and use device flow in the Web UI')}`);

  const ghPat = await password({ message: 'GitHub PAT (or Enter to skip):' });
  if (!isCancel(ghPat) && ghPat) {
    updateEnv('GH_TOKEN', ghPat);
    console.log(`  ${ICON.ok} GitHub PAT saved`);
  }

  // Done
  nl();
  box('SETUP COMPLETE', [
    C.success('Configuration saved to .env'),
    '',
    `${C.dim('Start')}      ${C.gold('docker compose up -d')}`,
    `${C.dim('Dashboard')}  ${C.gold('http://localhost')}`,
    `${C.dim('Status')}     ${C.gold('harbinger status')}`,
  ]);
  nl();
}

// ── harbinger setup-platform ────────────────────────────────────────────────
export function setupPlatform() {
  if (trySetupScript('setup-platform.mjs')) return;
  console.log(`\n  ${ICON.info} Platform setup script not found.`);
  console.log(`  Run ${C.gold('harbinger setup')} for full configuration.\n`);
}

// ── harbinger setup-telegram ────────────────────────────────────────────────
export async function setupTelegram() {
  if (trySetupScript('setup-telegram.mjs')) return;

  // Inline Telegram setup
  const { text, isCancel } = await import('@clack/prompts');
  console.log(`\n  ${ICON.info} Telegram Bot Setup\n`);
  console.log('  1. Open Telegram and message @BotFather');
  console.log('  2. Send /newbot and follow the prompts');
  console.log('  3. Copy the bot token\n');

  const botToken = await text({ message: 'Bot Token:', placeholder: '123456:ABC-DEF...' });
  if (isCancel(botToken)) { console.log('\nCancelled.\n'); process.exit(0); }

  const chatId = await text({ message: 'Chat ID:', placeholder: '-1001234567890' });
  if (isCancel(chatId)) { console.log('\nCancelled.\n'); process.exit(0); }

  updateEnv('TELEGRAM_BOT_TOKEN', botToken);
  updateEnv('TELEGRAM_CHAT_ID', chatId);

  console.log(`\n  ${ICON.ok} Telegram bot configured!`);
  console.log('  Restart the backend for changes to take effect.\n');
}

// ── harbinger setup-discord ─────────────────────────────────────────────────
export async function setupDiscord() {
  if (trySetupScript('setup-discord.mjs')) return;

  // Inline Discord setup fallback
  const { text, isCancel } = await import('@clack/prompts');
  console.log(`\n  ${ICON.info} Discord Bot Setup\n`);
  console.log('  1. Go to https://discord.com/developers/applications');
  console.log('  2. Create New Application → Bot → Copy Token');
  console.log('  3. Enable Message Content Intent');
  console.log('  4. Invite bot to server with Message + Slash Commands permissions\n');

  const botToken = await text({ message: 'Bot Token:', placeholder: 'MTIz...' });
  if (isCancel(botToken)) { console.log('\nCancelled.\n'); process.exit(0); }

  const guildId = await text({ message: 'Guild/Server ID:', placeholder: 'Right-click server → Copy ID' });
  if (isCancel(guildId)) { console.log('\nCancelled.\n'); process.exit(0); }

  const channelId = await text({ message: 'Channel ID:', placeholder: 'Right-click channel → Copy ID' });
  if (isCancel(channelId)) { console.log('\nCancelled.\n'); process.exit(0); }

  updateEnv('DISCORD_BOT_TOKEN', botToken);
  updateEnv('DISCORD_GUILD_ID', guildId);
  updateEnv('DISCORD_CHANNEL_ID', channelId);

  console.log(`\n  ${ICON.ok} Discord bot configured!`);
  console.log('  Restart the backend for changes to take effect.\n');
}

// ── harbinger reset-auth ────────────────────────────────────────────────────
export async function resetAuth() {
  const { randomBytes } = await import('crypto');

  const envPath = path.join(process.cwd(), '.env');
  if (!fs.existsSync(envPath)) {
    console.error(`\n  ${ICON.fail} No .env file found. Run ${C.gold('harbinger setup')} first.\n`);
    process.exit(1);
  }

  const newSecret = randomBytes(32).toString('base64');
  updateEnv('AUTH_SECRET', newSecret);
  console.log(`\n  ${ICON.ok} AUTH_SECRET regenerated.`);
  console.log('  All existing sessions have been invalidated.');
  console.log('  Restart your server for the change to take effect.\n');
}
