/**
 * harbinger onboard — First-run guided setup wizard (6 steps).
 */

import { execSync } from 'child_process';
import fs from 'fs';
import path from 'path';
import { C, ICON, box, step, nl, section, table } from '../ui.js';
import { markSetupDone } from '../config.js';
import { apiFetch, isBackendUp } from '../api.js';

function cmdExists(cmd) {
  try {
    execSync(`command -v ${cmd}`, { stdio: 'pipe' });
    return true;
  } catch {
    return false;
  }
}

export default async function onboardCommand() {
  const TOTAL = 6;

  box('HARBINGER ONBOARD', [
    C.dim('First-run guided setup wizard'),
    C.dim('This will configure your Harbinger instance'),
  ]);
  nl();

  // ── Step 1: Prerequisites ─────────────────────────────────────────────
  step(1, TOTAL, 'Checking prerequisites...');
  const tools = [
    { name: 'Node.js', cmd: 'node' },
    { name: 'pnpm', cmd: 'pnpm' },
    { name: 'Docker', cmd: 'docker' },
    { name: 'Go', cmd: 'go' },
  ];
  let allGood = true;
  for (const t of tools) {
    if (cmdExists(t.cmd)) {
      console.log(`    ${ICON.ok} ${t.name}`);
    } else {
      console.log(`    ${ICON.fail} ${t.name} — not found`);
      allGood = false;
    }
  }
  if (!allGood) {
    console.log(`\n  ${ICON.warn} Install missing tools before continuing.`);
    console.log(`  Then run ${C.gold('harbinger onboard')} again.\n`);
    return;
  }

  // ── Step 2: Environment generation ────────────────────────────────────
  step(2, TOTAL, 'Configuring environment...');
  const envPath = path.join(process.cwd(), '.env');
  if (!fs.existsSync(envPath)) {
    const { randomBytes } = await import('crypto');
    const jwtSecret = randomBytes(32).toString('hex');
    const dbPass = randomBytes(16).toString('hex');
    const authSecret = randomBytes(32).toString('base64');
    const redisPass = randomBytes(16).toString('hex');
    const neo4jPass = randomBytes(16).toString('hex');
    const env = `# Harbinger — generated by harbinger onboard
JWT_SECRET=${jwtSecret}
DB_PASSWORD=${dbPass}
REDIS_PASSWORD=${redisPass}
NEO4J_PASSWORD=${neo4jPass}
AUTH_SECRET=${authSecret}
AUTH_TRUST_HOST=true
DB_HOST=localhost
DB_PORT=5432
DB_NAME=harbinger
DB_USER=harbinger
REDIS_HOST=localhost
REDIS_PORT=6379
NEO4J_HOST=localhost
NEO4J_PORT=7687
`;
    fs.writeFileSync(envPath, env);
    console.log(`    ${ICON.ok} Generated .env with random secrets`);
  } else {
    console.log(`    ${ICON.ok} .env already exists`);
  }

  // ── Step 3: AI Provider selection ─────────────────────────────────────
  step(3, TOTAL, 'AI Provider setup...');
  console.log(`    ${ICON.info} Configure your AI provider in the Web UI:`);
  console.log(`    ${C.gold('Settings → Providers')} or run ${C.gold('harbinger setup')}`);
  console.log(`    ${C.dim('Supported: Anthropic, OpenAI, Groq, Ollama, LM Studio, GPT4All, Gemini, Mistral')}`);

  // ── Step 4: Authentication ────────────────────────────────────────────
  step(4, TOTAL, 'Authentication...');
  const ghToken = process.env.GH_TOKEN || '';
  if (ghToken) {
    console.log(`    ${ICON.ok} GH_TOKEN found in environment`);
  } else {
    const envContent = fs.existsSync(envPath) ? fs.readFileSync(envPath, 'utf-8') : '';
    if (envContent.match(/^GH_TOKEN=.+/m)) {
      console.log(`    ${ICON.ok} GH_TOKEN found in .env`);
    } else {
      console.log(`    ${ICON.warn} No GH_TOKEN configured.`);
      console.log(`    ${C.dim('Set it:')} ${C.gold('echo "GH_TOKEN=ghp_..." >> .env')}`);
      console.log(`    ${C.dim('Or use the device flow in the Web UI login page')}`);
    }
  }

  // ── Step 5: Docker services ───────────────────────────────────────────
  step(5, TOTAL, 'Docker services...');
  const up = await isBackendUp();
  if (up) {
    console.log(`    ${ICON.ok} Backend is already running`);
  } else {
    console.log(`    ${ICON.info} Start services with: ${C.gold('harbinger up')}`);
    console.log(`    ${C.dim('Or:')} ${C.gold('docker compose up -d')}`);
  }

  // ── Step 6: Agent roster ──────────────────────────────────────────────
  step(6, TOTAL, 'Agent roster...');
  if (up) {
    try {
      const agents = await apiFetch('/api/agents');
      const list = Array.isArray(agents) ? agents : (agents?.items || []);
      if (list.length > 0) {
        nl();
        const rows = list.map(a => [
          C.gold(a.codename || a.name || '?'),
          a.type || '?',
          a.description?.slice(0, 40) || '',
        ]);
        table(['CODENAME', 'TYPE', 'ROLE'], rows);
      } else {
        console.log(`    ${ICON.info} No agents yet. The backend seeds defaults on first run.`);
      }
    } catch {
      console.log(`    ${ICON.warn} Could not fetch agents.`);
    }
  } else {
    console.log(`    ${C.dim('Start the backend to see your agent roster')}`);
  }

  // ── Done ──────────────────────────────────────────────────────────────
  nl();
  markSetupDone();
  box('ONBOARDING COMPLETE', [
    C.success('Harbinger is configured.'),
    '',
    `${C.dim('Dashboard')}   ${C.gold('http://localhost:3000')}`,
    `${C.dim('Status')}      ${C.gold('harbinger status')}`,
    `${C.dim('Doctor')}      ${C.gold('harbinger doctor')}`,
    `${C.dim('Help')}        ${C.gold('harbinger --help')}`,
  ]);
  nl();
}
