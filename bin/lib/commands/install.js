/**
 * harbinger install — Full Docker environment bootstrap with progress.
 */

import { execSync, exec } from 'child_process';
import fs from 'fs';
import path from 'path';
import { C, ICON, box, step, nl, spinner, progressBar } from '../ui.js';

function run(cmd, opts = {}) {
  return execSync(cmd, { stdio: 'pipe', ...opts }).toString().trim();
}

function runLoud(cmd) {
  execSync(cmd, { stdio: 'inherit' });
}

function cmdExists(cmd) {
  try { run(`command -v ${cmd}`); return true; } catch { return false; }
}

export default async function installCommand(args = []) {
  const quick = args.includes('--quick');
  const noBuild = args.includes('--no-build');
  const servicesFlag = args.find(a => a.startsWith('--services='));
  const services = servicesFlag ? servicesFlag.split('=')[1].split(',') : null;

  const TOTAL_STEPS = noBuild ? 7 : 8;
  let current = 0;

  box('HARBINGER INSTALL', [
    C.dim('Full environment bootstrap'),
    quick ? C.warn('Quick mode — using defaults') : '',
    noBuild ? C.dim('Skipping build step') : '',
  ].filter(Boolean));
  nl();

  // ── Step 1: Prerequisites ─────────────────────────────────────────────
  current++;
  step(current, TOTAL_STEPS, 'Checking prerequisites...');
  const prereqs = [
    { name: 'Node.js', cmd: 'node', check: () => parseInt(process.version.slice(1)) >= 18 },
    { name: 'pnpm', cmd: 'pnpm' },
    { name: 'Docker', cmd: 'docker' },
    { name: 'Docker Compose', check: () => { run('docker compose version'); return true; } },
  ];

  for (const p of prereqs) {
    if (p.check) {
      try {
        if (!p.check()) throw new Error();
      } catch {
        console.error(`  ${ICON.fail} ${p.name} check failed. Install it and retry.`);
        process.exit(1);
      }
    } else if (!cmdExists(p.cmd)) {
      console.error(`  ${ICON.fail} ${p.name} not found. Install it and retry.`);
      process.exit(1);
    }
    console.log(`    ${ICON.ok} ${p.name}`);
  }

  // ── Step 2: Generate .env ─────────────────────────────────────────────
  current++;
  step(current, TOTAL_STEPS, 'Configuring environment...');
  const envPath = path.join(process.cwd(), '.env');
  if (!fs.existsSync(envPath)) {
    const { randomBytes } = await import('crypto');
    const jwtSecret = randomBytes(32).toString('hex');
    const dbPass = randomBytes(16).toString('hex');
    const authSecret = randomBytes(32).toString('base64');
    const env = `# Harbinger — generated by harbinger install
JWT_SECRET=${jwtSecret}
DB_PASSWORD=${dbPass}
AUTH_SECRET=${authSecret}
AUTH_TRUST_HOST=true
DB_HOST=localhost
DB_PORT=5432
DB_NAME=harbinger
DB_USER=harbinger
REDIS_HOST=localhost
REDIS_PORT=6379
NEO4J_HOST=localhost
NEO4J_PORT=7687
`;
    fs.writeFileSync(envPath, env);
    console.log(`    ${ICON.ok} Generated .env with random secrets`);
  } else {
    console.log(`    ${ICON.ok} .env already exists`);
  }

  // ── Step 3: Docker network ────────────────────────────────────────────
  current++;
  step(current, TOTAL_STEPS, 'Setting up Docker network...');
  try {
    run('docker network inspect harbinger-net');
    console.log(`    ${ICON.ok} Network harbinger-net exists`);
  } catch {
    try {
      run('docker network create harbinger-net');
      console.log(`    ${ICON.ok} Created network harbinger-net`);
    } catch {
      console.log(`    ${ICON.warn} Could not create network (may already exist)`);
    }
  }

  // ── Step 4: Pull images ───────────────────────────────────────────────
  current++;
  step(current, TOTAL_STEPS, 'Pulling Docker images...');
  try {
    const svcArg = services ? services.join(' ') : '';
    runLoud(`docker compose pull ${svcArg}`);
    console.log(`    ${ICON.ok} Images pulled`);
  } catch {
    console.log(`    ${ICON.warn} Some images failed to pull (may build locally)`);
  }

  // ── Step 5: Build ─────────────────────────────────────────────────────
  if (!noBuild) {
    current++;
    step(current, TOTAL_STEPS, 'Building services...');
    try {
      const svcArg = services ? services.join(' ') : '';
      runLoud(`docker compose build ${svcArg}`);
      console.log(`    ${ICON.ok} Build complete`);
    } catch (e) {
      console.error(`    ${ICON.fail} Build failed. Check docker-compose.yml.`);
      process.exit(1);
    }
  }

  // ── Step 6: Start stack ───────────────────────────────────────────────
  current++;
  step(current, TOTAL_STEPS, 'Starting services...');
  try {
    const svcArg = services ? services.join(' ') : '';
    runLoud(`docker compose up -d ${svcArg}`);
    console.log(`    ${ICON.ok} Services started`);
  } catch {
    console.error(`    ${ICON.fail} Failed to start services.`);
    process.exit(1);
  }

  // ── Step 7: Wait for healthy ──────────────────────────────────────────
  current++;
  step(current, TOTAL_STEPS, 'Waiting for backend health...');
  let healthy = false;
  for (let attempt = 0; attempt < 30; attempt++) {
    try {
      const resp = await fetch('http://localhost:8080/health', { signal: AbortSignal.timeout(2000) });
      if (resp.ok) { healthy = true; break; }
    } catch {}
    await new Promise(r => setTimeout(r, 2000));
    process.stdout.write('.');
  }
  console.log();
  if (healthy) {
    console.log(`    ${ICON.ok} Backend is healthy`);
  } else {
    console.log(`    ${ICON.warn} Backend did not respond within 60s. Check logs: ${C.gold('harbinger logs backend')}`);
  }

  // ── Step 8: Done ──────────────────────────────────────────────────────
  current++;
  step(current, TOTAL_STEPS, 'Opening browser...');
  try {
    const open = (await import('open')).default;
    await open('http://localhost:3000');
    console.log(`    ${ICON.ok} Opened http://localhost:3000`);
  } catch {
    console.log(`    ${ICON.info} Open ${C.gold('http://localhost:3000')} in your browser`);
  }

  nl();
  box('INSTALL COMPLETE', [
    `${C.success('Harbinger is running.')}`,
    '',
    `Dashboard:  ${C.gold('http://localhost:3000')}`,
    `API:        ${C.gold('http://localhost:8080')}`,
    '',
    `Next: ${C.gold('harbinger onboard')} to configure agents & providers`,
  ]);
  nl();
}
